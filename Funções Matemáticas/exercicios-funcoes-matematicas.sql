--EXERCÍCIOS COM FUNÇÕES MATEMÁTICAS

--ABS:
SELECT ABS(-10),
	   ABS(0.0),
	   ABS(-9.0),
	   ABS(1.5);

--RAND
SELECT RAND(), 
	   RAND(), 
	   RAND();

--EXEMPLO
DECLARE @COUNT SMALLINT
SET @COUNT = 1
WHILE @COUNT < 10
	BEGIN
	SELECT RAND() AS RAND_NUMBER, @COUNT AS COUNT
	SET @COUNT = @COUNT + 1
	END;

